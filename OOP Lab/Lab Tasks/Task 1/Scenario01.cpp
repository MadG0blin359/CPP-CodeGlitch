#include <iostream>
using namespace std;

int main() {

    cout << "size of int : " << sizeof(int) << "byte" << endl;
    cout << "size of char : " << sizeof(char) << "byte" << endl;
    cout << "size of float : " << sizeof(float) << "byte" << endl;
    cout << "size of long : " << sizeof(long) << "byte" << endl;
    cout << "size of double : " << sizeof(double) << "byte" << endl;

    return 0;
}